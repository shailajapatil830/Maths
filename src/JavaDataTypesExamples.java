
public class JavaDataTypesExamples {
	public static void main(String[] args) {
		boolean b1 = true;
		boolean b2 = false;
		boolean b3 = (10>2)? true : false;
		
		System.out.println("Value of boolean variable b1 is : " + b1);
		System.out.println("Value of boolean variable b2 is : " + b2);
		System.out.println("Value of boolean variable b3 is : " + b3);
		
		// byte data types//
		
		byte r1 = 100;
		byte r2 = 80;
		byte r3 = 20;
		
		System.out.println("Value of byte r1 is : " + r1);
		System.out.println("Value of byte r2 is : " + r2);
		System.out.println("Value of byte r3 is : " + r3);
	}

}
