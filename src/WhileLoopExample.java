
public class WhileLoopExample {
      public static void main(String[]arges) {
    	  System.out.println("for Loop");
    	  for(int i = 0; i < 10; i++) {
    		  System.out.println("Hello " + i);
    	  }
    	  
    	  System.out.println("while Loop");
    	  int i = 0;
    	  while(i < 10) {
    		  System.out.println("Hello " + i);
    		  i++;
    	  }
    	  
    	  
    	  System.out.println("do while Loop");
    	  i = 0;
    	  do
    	  {
    		  System.out.println("Hello" + i);
    		  i++;
    	  }while(i < 10);
      }
}
